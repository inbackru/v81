def create_articles():
    print("Creating SEO articles batch 4...")
    with app.app_context():
        admin = Admin.query.first()
        if not admin:
            print("No admin found")
            return

        created = 0
        for art in ARTICLES:
            existing = BlogPost.query.filter_by(title=art['title']).first()
            if existing:
                print(f"  Skip (exists): {art['title'][:60]}")
                continue

            slug = slugify(art['title'])
            slug_exists = BlogPost.query.filter_by(slug=slug).first()
            if slug_exists:
                slug = slug + '-' + str(random.randint(100, 999))

            post = BlogPost(
                title=art['title'],
                slug=slug,
                content=art['content'],
                excerpt=art['excerpt'],
                category=art['category'],
                author_id=admin.id,
                status='published',
                meta_title=art['meta_title'],
                meta_description=art['meta_description'],
                tags=art['tags'],
                featured_image='https://images.unsplash.com/photo-1560518883-ce09059eeffa?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&q=80',
                published_at=datetime.utcnow() - timedelta(days=random.randint(1, 90)),
                views_count=random.randint(100, 1200),
                likes_count=random.randint(10, 120)
            )
            db.session.add(post)
            created += 1
            print(f"  + {art['title'][:70]}")

        db.session.commit()
        total = BlogPost.query.count()
        published = BlogPost.query.filter_by(status='published').count()
        print(f"\nCreated: {created} articles")
        print(f"Total: {total} | Published: {published}")

if __name__ == '__main__':
    create_articles()
