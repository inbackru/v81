def create_seo_articles():
    with app.app_context():
        admin = Admin.query.first()
        if not admin:
            print("No admin found!")
            return

        categories_map = {}
        for cat in Category.query.all():
            categories_map[cat.name] = cat

        articles = get_articles()
        created = 0
        skipped = 0

        for i, article_data in enumerate(articles):
            slug = slugify(article_data['title'])

            existing = BlogPost.query.filter_by(slug=slug).first()
            if existing:
                print(f"  Skipped (exists): {article_data['title'][:60]}...")
                skipped += 1
                continue

            cat_name = article_data.get('category', 'Советы покупателям')
            category_obj = categories_map.get(cat_name)

            post = BlogPost(
                title=article_data['title'],
                slug=slug,
                content=article_data['content'],
                excerpt=article_data['excerpt'],
                meta_title=article_data['meta_title'],
                meta_description=article_data['meta_description'],
                meta_keywords=article_data.get('meta_keywords', ''),
                status='published',
                category_id=category_obj.id if category_obj else None,
                category=cat_name,
                tags=article_data.get('tags', '[]'),
                author_id=admin.id,
                published_at=datetime.utcnow() - timedelta(days=random.randint(1, 30), hours=random.randint(0, 23)),
                views_count=random.randint(50, 500),
            )
            db.session.add(post)
            created += 1
            print(f"  Created: {article_data['title'][:70]}...")

        db.session.commit()

        total = BlogPost.query.filter_by(status='published').count()
        print(f"\nDone! Created: {created}, Skipped: {skipped}, Total published: {total}")


if __name__ == '__main__':
    create_seo_articles()
