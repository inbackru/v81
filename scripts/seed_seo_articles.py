def seed_articles():
    with app.app_context():
        created = 0
        skipped = 0
        for article_data in ARTICLES:
            existing = BlogPost.query.filter_by(slug=article_data["slug"]).first()
            if existing:
                print(f"  SKIP (exists): {article_data['title'][:60]}")
                skipped += 1
                continue
            
            post = BlogPost(
                title=article_data["title"],
                slug=article_data["slug"],
                content=article_data["content"],
                excerpt=article_data["excerpt"],
                meta_title=article_data["meta_title"],
                meta_description=article_data["meta_description"],
                meta_keywords=article_data["meta_keywords"],
                category_id=article_data["category_id"],
                author_id=1,
                status="published",
                published_at=datetime.utcnow() - timedelta(days=len(ARTICLES) - created),
                views_count=0,
            )
            db.session.add(post)
            created += 1
            print(f"  CREATED: {article_data['title'][:60]}")
        
        db.session.commit()
        print(f"\nDone! Created: {created}, Skipped: {skipped}")

if __name__ == "__main__":
    seed_articles()
